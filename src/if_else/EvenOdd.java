package if_else;

// Check whether the number is even or odd
public class EvenOdd {
    public static void main (String[] args){
        int num = 99;
        if(num % 2 == 0){
            System.out.println("It is an even number");
        }
        else{
            System.out.println("It is an odd number.");
        }
    }
}
